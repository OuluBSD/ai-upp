print("If-Else")
x = 10
if x > 5:
    print("x is greater than 5")
else:
    print("x is not greater than 5")

print("While Loop")
count = 0
while count < 3:
    print(count)
    count = count + 1

print("For Loop")
for i in range(3):
    print(i)

print("For Loop with list")
l = [10, 20, 30]
for x in l:
    print(x)
